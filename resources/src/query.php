<?php

function queryTrigger() {
    $response = new WP_REST_Response(runSqlQuery("SELECT * FROM wp_postmeta WHERE meta_id = 3"));
    $response->set_status(200);
    return $response;
  }

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  function runSqlQuery($qry) {
      global $wpdb;
      return $wpdb->get_results($qry);
  }
