<?php

function getModifiedPrice($price, $mod) {
    if(!$mod) {
        $mod = 0;
    }
      $modprice = $price + $mod;
      return $modprice;
  }

  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  function ifDeco($deco) {
      if(!$deco || empty($deco)) {
          return "NA";
      }
      return $deco;
  }

  
  ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

  function dataToArray($data) {
      $tmpArray = array();
    for ($i = 0; $i < count($data); $i++) {
        $tmpArray[$i] = $data[$i];
    }
    return $tmpArray;
  }

